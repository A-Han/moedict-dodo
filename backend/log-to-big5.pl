use utf8;
use 5.12.0;
binmode STDOUT, ':encoding(cp950)';

my $count;
for my $x (@{X()}) {
    my $name = $x->{詞目名稱};
    my $text = $x->{意見說明};
    my $xref = $x->{參考資料};
    $text =~ s/：/：\n/g;
    $text =~ s/』可是/』\n可是/g;
    $text =~ s/(.{33})(.)/$1\n$2/;
    $text .= "\n" until $text =~ /\n.*\n/;
    $xref =~ s/本辭典的「/「/g;
    $xref .= "\n（是否應改為「或作、或稱為」？)" if $xref =~ /「亦作、亦稱/;
    $xref =~ s/(.{33})(.)/$1\n$2/;
    $xref .= "\n" until $xref =~ /\n.*\n/;
    $text =~ s/\n/\n                /g;
    $xref =~ s/\n/\n                /g;
    if ($count == 0) {
        say "                " for 1..9;
    }

    print << ".";
                $name
                
                $text
                
                $xref
                
.

    $count++;
    if ($count == 5) {
        say "\f";
        $count = 0;
    }
}

